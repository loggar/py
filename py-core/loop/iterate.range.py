# range(stop)
for i in range(3):
    print(i)

# range(start, stop)
for i in range(1, 8):
    print(i)

# range(start, stop, step)
for i in range(3, 16, 3):
    print(i)

# range(len)
languages = ['Spanish', 'English',  'French', 'German', 'Irish', 'Chinese']

for index in range(len(languages)):
    print('Language:', languages[index])

# range index
for index in range(6):
    print('Language:', languages[index])
